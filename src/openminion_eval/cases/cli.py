"""CLI entry for running starter EvalCases and emitting Markdown.

Usage:

    python -m openminion_eval.cases [--out PATH] [--category CATEGORY]

The Make target ``make eval`` invokes this with no arguments and writes
the Markdown report to stdout. ``--out`` writes to a file.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

from openminion_eval.cases.registry import (
    EvalCaseResult,
    GradeOutcome,
    grade_case,
    registered_cases,
)


def _render_markdown(results: list[EvalCaseResult]) -> str:
    """Render the eval results as a Markdown report."""
    lines: list[str] = []
    lines.append("# OpenMinion Eval Report")
    lines.append("")
    lines.append("Generated by `openminion_eval.cases`.")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    counts: dict[str, int] = {}
    for outcome in GradeOutcome:
        counts[outcome.value] = sum(1 for r in results if r.outcome is outcome)
    lines.append(f"- Total cases: {len(results)}")
    for outcome in GradeOutcome:
        if counts[outcome.value]:
            lines.append(f"- {outcome.value}: {counts[outcome.value]}")
    lines.append("")
    lines.append("## Cases")
    lines.append("")
    lines.append("| Case | Category | Mode | Outcome | Detail |")
    lines.append("| --- | --- | --- | --- | --- |")
    for r in results:
        detail = r.detail.replace("|", "\\|") if r.detail else ""
        lines.append(
            f"| `{r.case_id}` | {r.category} | {r.grade_mode.value} | "
            f"{r.outcome.value} | {detail} |"
        )
    lines.append("")
    return "\n".join(lines)


def _exit_code_from(results: list[EvalCaseResult]) -> int:
    """Return 0 if no case explicitly FAILed; 1 otherwise.

    SKIPPED and UNGRADED do not fail the build; they indicate the case
    is environment-gated or human-graded.
    """
    return 1 if any(r.outcome is GradeOutcome.FAIL for r in results) else 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(prog="openminion_eval.cases")
    parser.add_argument(
        "--out",
        type=Path,
        default=None,
        help="write the Markdown report to PATH instead of stdout",
    )
    parser.add_argument(
        "--category",
        default=None,
        help="filter cases by category (coding / research / recovery / "
        "multi_tool / memory)",
    )
    args = parser.parse_args(argv)

    cases = registered_cases()
    if args.category:
        cases = tuple(c for c in cases if c.category == args.category)

    results = [grade_case(c) for c in cases]
    report = _render_markdown(results)

    if args.out is not None:
        args.out.parent.mkdir(parents=True, exist_ok=True)
        args.out.write_text(report, encoding="utf-8")
    else:
        sys.stdout.write(report + "\n")

    return _exit_code_from(results)


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
